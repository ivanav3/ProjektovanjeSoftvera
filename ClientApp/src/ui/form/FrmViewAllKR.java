    private void txtPretragaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPretragaActionPerformed
        try {
            // TODO add your handling code here:
            String naziv = txtMesto.getText();

            List<KomunalniRadnik> filtrirani;
            if (naziv == null || naziv.isEmpty() || !naziv.matches("[a-zA-Z]+")) {
                filtrirani = null;
            } else {
                filtrirani = Controller.getInstance().searchKR(naziv);
            }
            TableModel tm = new KRTableModel(filtrirani);
            tblKR.setModel(tm);
            tblKR.getTableHeader().setReorderingAllowed(false);
            if (filtrirani == null || filtrirani.size() == 0) {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje komunalne radnike po zadatoj vrednosti!");

            } else {
                JOptionPane.showMessageDialog(this, "Sistem je nasao komunalne radnike po zadatoj vrednosti!");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje komunalne radnike po zadatoj vrednosti!");

        }
    }//GEN-LAST:event_txtPretragaActionPerformed


