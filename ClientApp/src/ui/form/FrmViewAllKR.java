    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        // TODO add your handling code here:
        try {
            int red = tblKR.getSelectedRow();
            if (red == -1) {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita komunalnog radnika!");

            } else {

                KRTableModel krtm = (KRTableModel) tblKR.getModel();
                KomunalniRadnik kr = krtm.getRow(red);
                System.out.println("Ovde je " + kr.getKomunalniRadnikID());
                kr = Controller.getInstance().getKR(kr);
                new FrmNewEditKR(null, true, VIEW_KR, kr).setVisible(true);
//                JOptionPane.showMessageDialog(this, "Sistem je ucitao komunalnog radnika!");

            }
            populateTableKR();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita komunalnog radnika!");

        }
    }//GEN-LAST:event_btnDetaljiActionPerformed

