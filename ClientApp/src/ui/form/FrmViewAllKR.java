private void populateTableKR() throws Exception {
        List<KomunalniRadnik> komunalniRadnici = Controller.getInstance().getAllKomunalniRandnici();

        TableModel tm = new KRTableModel(komunalniRadnici);
        tblKR.setModel(tm);
        tblKR.getTableHeader().setReorderingAllowed(false);
        tblKR.getTableHeader().setResizingAllowed(false);
        tblKR.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
    }
