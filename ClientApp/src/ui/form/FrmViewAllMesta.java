    private void txtPretragaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPretragaActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            String naziv = txtDrzava.getText();

            List<Mesto> filtrirani;
            if (naziv == null || naziv.isEmpty() || !naziv.matches("[a-zA-Z]+")) {
                filtrirani = null;
            } else {
                filtrirani = Controller.getInstance().searchMesta(naziv);
            }
            TableModel tm = new MestoTableModel(filtrirani);
            tblMesta.setModel(tm);
            tblMesta.getTableHeader().setReorderingAllowed(false);

            if (filtrirani == null || filtrirani.size() == 0) {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje mesta po zadatoj vrednosti!");

            } else {
                JOptionPane.showMessageDialog(this, "Sistem je nasao mesta po zadatoj vrednosti!");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje mesta po zadatoj vrednosti!");

        }

    }//GEN-LAST:event_txtPretragaActionPerformed


