private void populateTableMesta() throws Exception {
        List<Mesto> mesta = Controller.getInstance().getAllMesta();
        TableModel tm = new MestoTableModel(mesta);
        tblMesta.setModel(tm);
        tblMesta.getTableHeader().setReorderingAllowed(false);
        tblMesta.getTableHeader().setResizingAllowed(false);

        tblMesta.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }
