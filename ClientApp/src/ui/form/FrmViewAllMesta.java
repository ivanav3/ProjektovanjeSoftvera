    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        // TODO add your handling code here:
        try {
            int red = tblMesta.getSelectedRow();
            if (red == -1) {
//                JOptionPane.showMessageDialog(this, "Niste izabrali komunalnog radnika!");
                JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita mesto!");

            } else {

                MestoTableModel mtm = (MestoTableModel) tblMesta.getModel();
                Mesto m = mtm.getRow(red);
                System.out.println("Ovde je " + m.getMestoID());
                new FrmNewEditMesto(null, true, VIEW_M, m).setVisible(true);

            }
            populateTableMesta();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita mesto!");

        }

    }//GEN-LAST:event_btnDetaljiActionPerformed

